// Хелпер для получения количества статей по категории
export const getCategoryCount = (categoryId: CategoryId): number => {
  if (categoryId === 'all') return blogPosts.length;
  return blogPosts.filter(post => post.categoryId === categoryId).length;
};

// Хелпер для пагинации
export const getPaginatedPosts = (posts: BlogPost[], page: number, perPage: number = 9): BlogPost[] => {
  const start = (page - 1) * perPage;
  return posts.slice(start, start + perPage);
};

// Хелпер для поиска
export const searchPosts = (query: string): BlogPost[] => {
  const lowerQuery = query.toLowerCase();
  return blogPosts.filter(post =>
    post.title.toLowerCase().includes(lowerQuery) ||
    post.excerpt.toLowerCase().includes(lowerQuery) ||
    post.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};
