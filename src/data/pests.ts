// Получить вредителя по slug
export function getPestBySlug(slug: string): Pest | undefined {
  return pests.find(p => p.slug === slug);
}

// Получить вредителей для конкретной услуги
export function getPestsByService(serviceType: 'dezinsekciya' | 'deratizaciya'): Pest[] {
  return pests.filter(p => p.serviceType === serviceType);
}

// Slug-массивы для SSG
export const pestSlugs = pests.map(p => p.slug);
export const dezinsekciyaPestSlugs = pests.filter(p => p.serviceType === 'dezinsekciya').map(p => p.slug);
export const deratizaciyaPestSlugs = pests.filter(p => p.serviceType === 'deratizaciya').map(p => p.slug);
