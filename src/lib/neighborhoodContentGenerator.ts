function generateIntro(neighborhood: Neighborhood, districtType: DistrictType, rng: SeededRandom): string {
  const baseIntro = rng.choice(introTemplates[districtType]);
  const responseTimeNote = neighborhood.responseTime 
    ? ` Время прибытия специалиста — ${neighborhood.responseTime}.`
    : '';
  
  return baseIntro + responseTimeNote;
}

function generateWhyUs(neighborhood: Neighborhood, districtType: DistrictType, rng: SeededRandom): string[] {
  const baseItems = rng.sample(whyUsTemplates[districtType], 4);
  
  // Добавляем персонализированный пункт о времени выезда
  if (neighborhood.responseTime) {
    baseItems.push(`Быстрый выезд — ${neighborhood.responseTime} в ваш район`);
  }
  
  return baseItems;
}

function generateCoverage(neighborhood: Neighborhood): string {
  const landmarks = neighborhood.landmarks.slice(0, 4).join(', ');
  const streets = neighborhood.streets?.slice(0, 3).join(', ') || '';
  
  let coverage = `Обслуживаем всю территорию района ${neighborhood.name}`;
  
  if (landmarks) {
    coverage += `, включая ${landmarks}`;
  }
  
  if (streets) {
    coverage += `. Выезжаем на ${streets} и прилегающие улицы`;
  }
  
  coverage += '.';
  
  return coverage;
}

function generateAdvantages(districtType: DistrictType, rng: SeededRandom): string[] {
  return rng.sample(advantagesTemplates[districtType], 4);
}

// =============================================
// ГЛАВНАЯ ФУНКЦИЯ ГЕНЕРАЦИИ
// =============================================

export function generateNeighborhoodContent(neighborhood: Neighborhood): NeighborhoodContent {
  const districtType = getDistrictType(neighborhood.districtId);
  const seed = simpleHash(neighborhood.slug);
  const rng = new SeededRandom(seed);
  
  return {
    slug: neighborhood.slug,
    name: neighborhood.name,
    intro: generateIntro(neighborhood, districtType, rng),
    whyUs: generateWhyUs(neighborhood, districtType, rng),
    coverage: generateCoverage(neighborhood),
    landmarks: neighborhood.landmarks,
    advantages: generateAdvantages(districtType, rng)
  };
}
