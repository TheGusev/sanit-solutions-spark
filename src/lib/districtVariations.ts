// Типы округов по характеру
export type DistrictType = 'elite' | 'residential' | 'business' | 'industrial' | 'suburban';

// Маппинг округов на типы
const districtTypeMap: Record<string, DistrictType> = {
  'cao': 'elite',        // ЦАО — элитный центр
  'zao': 'business',     // ЗАО — бизнес-класс
  'svao': 'residential', // СВАО — спальные районы
  'vao': 'industrial',   // ВАО — промышленные зоны
  'uvao': 'residential', // ЮВАО — жилые районы
  'uao': 'suburban',     // ЮАО — пригород
  'uzao': 'business',    // ЮЗАО — бизнес + жильё
  'sao': 'residential',  // САО — жилые районы
  'szao': 'business'     // СЗАО — бизнес-класс
};

// Определяем тип округа по slug
export function getDistrictType(slug: string): DistrictType {
  return districtTypeMap[slug.toLowerCase()] || 'residential';
}

